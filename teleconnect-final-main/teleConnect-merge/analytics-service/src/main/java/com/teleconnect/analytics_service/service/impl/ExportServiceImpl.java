    // ─── Saved Report Export ────────────────────────────────────────────────

    @Override
    public byte[] exportReportAsPdf(Long reportId) throws IOException {
        log.info("Exporting report as PDF reportId={}", reportId);
        TelecomReport report = findReport(reportId);
        Map<String, Object> metrics = parseMetrics(report.getMetrics());

        Map<String, String> rows = new LinkedHashMap<>();
        rows.put("Report ID", String.valueOf(report.getReportId()));
        rows.put("Scope", report.getScope().name());
        rows.put("Scope Value", report.getScopeValue());
        rows.put("Period Start", report.getPeriodStart().toString());
        rows.put("Period End", report.getPeriodEnd().toString());
        rows.put("Generated Date", report.getGeneratedDate().toString());
        rows.put("Generated By", report.getGeneratedBy() != null ? report.getGeneratedBy().toString() : "System");
        rows.put("──────────", "──────────────────");
        for (Map.Entry<String, Object> e : metrics.entrySet()) {
            rows.put(e.getKey(), String.valueOf(e.getValue()));
        }

        return buildPdf("TeleConnect Analytics Report", "Module 2.7 — Telecom Analytics & Reporting", rows);
    }

    @Override
    public byte[] exportReportAsCsv(Long reportId) {
        log.info("Exporting report as CSV reportId={}", reportId);
        TelecomReport report = findReport(reportId);
        Map<String, Object> metrics = parseMetrics(report.getMetrics());

        StringBuilder sb = new StringBuilder();
        sb.append("Field,Value\n");
        sb.append("Report ID,").append(report.getReportId()).append("\n");
        sb.append("Scope,").append(report.getScope()).append("\n");
        sb.append("Scope Value,").append(csvEscape(report.getScopeValue())).append("\n");
        sb.append("Period Start,").append(report.getPeriodStart()).append("\n");
        sb.append("Period End,").append(report.getPeriodEnd()).append("\n");
        sb.append("Generated Date,").append(report.getGeneratedDate()).append("\n");
        sb.append("Generated By,").append(report.getGeneratedBy() != null ? report.getGeneratedBy() : "System").append("\n");
        for (Map.Entry<String, Object> e : metrics.entrySet()) {
            sb.append(csvEscape(e.getKey())).append(",").append(csvEscape(String.valueOf(e.getValue()))).append("\n");
        }
        return sb.toString().getBytes();
    }

    @Override
    public byte[] exportDashboardAsPdf(DashboardResponse data) throws IOException {
        log.info("Exporting dashboard as PDF");
        Map<String, String> rows = new LinkedHashMap<>();

        if (data.getKpis() != null) {
            DashboardResponse.KPIMetrics k = data.getKpis();
            rows.put("Active Subscribers", String.valueOf(k.getActiveSubscribers()));
            rows.put("Churn Rate (%)", String.valueOf(k.getChurnRate()));
            rows.put("ARPU", String.valueOf(k.getArpu()));
            rows.put("Collection Efficiency (%)", String.valueOf(k.getCollectionEfficiency()));
            rows.put("SLA Compliance (%)", String.valueOf(k.getSlaCompliance()));
            rows.put("Data Consumption (MB)", String.valueOf(k.getDataConsumption()));
            rows.put("Fault Count", String.valueOf(k.getFaultCount()));
            rows.put("Dispute Rate (%)", String.valueOf(k.getDisputeRate()));
        }

        rows.put("──────────", "──────────────────");
        rows.put("Graphs Included", "6");
        rows.put("1", "Data Consumption Trend");
        rows.put("2", "ARPU by Account Type");
        rows.put("3", "Churn Distribution by Segment");
        rows.put("4", "Overdue Invoices by Age");
        rows.put("5", "Subscriber Growth Trend");
        rows.put("6", "Fault Frequency by Priority");

        if (data.getRegions() != null && data.getRegions().getSubscribersByRegion() != null) {
            rows.put("────────── Region ──────────", "──────────────────");
            data.getRegions().getSubscribersByRegion().forEach((region, value) ->
                    rows.put("Subscribers - " + region, String.valueOf(value)));
        }

        if (data.getSegments() != null && data.getSegments().getSubscribersByAccountType() != null) {
            rows.put("────────── Segment ──────────", "──────────────────");
            data.getSegments().getSubscribersByAccountType().forEach((type, value) ->
                    rows.put("Subscribers - " + type, String.valueOf(value)));
        }

        return buildPdf("TeleConnect Executive Dashboard", "Module 4.7 — Telecom Analytics & Reporting", rows);
    }


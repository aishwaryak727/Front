    // ─── PDF builder ──────────────────────────────────────────────────────────

    private byte[] buildPdf(String title, String subtitle, Map<String, String> rows) throws IOException {
        log.debug("Building PDF document title='{}' subtitle='{}' rows={}", title, subtitle, rows.size());
        try (PDDocument doc = new PDDocument()) {
            PDPage page = new PDPage(PDRectangle.A4);
            doc.addPage(page);

            PDType1Font fontBold = new PDType1Font(Standard14Fonts.FontName.HELVETICA_BOLD);
            PDType1Font fontRegular = new PDType1Font(Standard14Fonts.FontName.HELVETICA);

            float pageHeight = PDRectangle.A4.getHeight();
            float yStart = pageHeight - MARGIN;

            try (PDPageContentStream cs = new PDPageContentStream(doc, page)) {
                float y = yStart;

                // Header bar
                cs.setNonStrokingColor(0.18f, 0.47f, 0.71f);
                cs.addRect(MARGIN, y - 40, CONTENT_WIDTH, 40);
                cs.fill();

                // Title
                cs.setNonStrokingColor(1f, 1f, 1f);
                cs.beginText();
                cs.setFont(fontBold, 16);
                cs.newLineAtOffset(MARGIN + 10, y - 27);
                cs.showText(title);
                cs.endText();

                y -= 55;

                // Subtitle
                cs.setNonStrokingColor(0.3f, 0.3f, 0.3f);
                cs.beginText();
                cs.setFont(fontRegular, 10);
                cs.newLineAtOffset(MARGIN, y);
                cs.showText(subtitle);
                cs.endText();

                y -= 8;

                // Divider line
                cs.setStrokingColor(0.18f, 0.47f, 0.71f);
                cs.setLineWidth(1f);
                cs.moveTo(MARGIN, y);
                cs.lineTo(MARGIN + CONTENT_WIDTH, y);
                cs.stroke();

                y -= LINE_HEIGHT + 4;

                // Rows
                float colLabel = MARGIN + 10;
                float colValue = MARGIN + 230;
                boolean shade = false;

                for (Map.Entry<String, String> entry : rows.entrySet()) {
                    if (y < MARGIN + LINE_HEIGHT) {
                        cs.close();
                        PDPage newPage = new PDPage(PDRectangle.A4);
                        doc.addPage(newPage);
                        // reopen on new page — simplified: just continue
                        break;
                    }

                    if (entry.getKey().startsWith("──")) {
                        cs.setStrokingColor(0.8f, 0.8f, 0.8f);
                        cs.setLineWidth(0.5f);
                        cs.moveTo(MARGIN, y - 2);
                        cs.lineTo(MARGIN + CONTENT_WIDTH, y - 2);
                        cs.stroke();
                        y -= LINE_HEIGHT * 0.6f;
                        shade = false;
                        continue;
                    }

                    if (shade) {
                        cs.setNonStrokingColor(0.95f, 0.97f, 1f);
                        cs.addRect(MARGIN, y - LINE_HEIGHT + 4, CONTENT_WIDTH, LINE_HEIGHT);
                        cs.fill();
                    }
                    shade = !shade;

                    cs.setNonStrokingColor(0.2f, 0.2f, 0.2f);
                    cs.beginText();
                    cs.setFont(fontBold, 9);
                    cs.newLineAtOffset(colLabel, y - 4);
                    cs.showText(entry.getKey());
                    cs.endText();

                    cs.setNonStrokingColor(0.1f, 0.1f, 0.1f);
                    cs.beginText();
                    cs.setFont(fontRegular, 9);
                    cs.newLineAtOffset(colValue, y - 4);
                    cs.showText(entry.getValue() != null ? entry.getValue() : "—");
                    cs.endText();

                    y -= LINE_HEIGHT;
                }

                // Footer
                float footerY = MARGIN - 10;
                cs.setNonStrokingColor(0.6f, 0.6f, 0.6f);
                cs.beginText();
                cs.setFont(fontRegular, 7);
                cs.newLineAtOffset(MARGIN, footerY);
                cs.showText("TeleConnect — Module 2.7 Analytics & Reporting  |  Generated: "
                        + java.time.LocalDateTime.now().toString().substring(0, 19));
                cs.endText();
            }

            ByteArrayOutputStream out = new ByteArrayOutputStream();
            doc.save(out);
            return out.toByteArray();
        }
    }

    // ─── Helpers ──────────────────────────────────────────────────────────────

    private TelecomReport findReport(Long reportId) {
        log.debug("Looking up report reportId={}", reportId);
        return telecomReportRepository.findById(reportId)
                .orElseThrow(() -> new ResourceNotFoundException("Report not found with ID: " + reportId));
    }

    private Map<String, Object> parseMetrics(String metricsJson) {
        log.debug("Parsing metrics json length={}", metricsJson != null ? metricsJson.length() : 0);
        try {
            return objectMapper.readValue(metricsJson, new TypeReference<>() {});
        } catch (Exception e) {
            log.error("Failed to parse report metrics JSON", e);
            throw new AnalyticsException("Failed to parse report metrics: " + e.getMessage());
        }
    }

    private String csvEscape(String value) {
        if (value == null) return "";
        if (value.contains(",") || value.contains("\"") || value.contains("\n")) {
            return "\"" + value.replace("\"", "\"\"") + "\"";
        }
        return value;
    }
